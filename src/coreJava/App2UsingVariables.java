package coreJava;

/**
 * Programa que fará a demonstração do uso de variáveis.
 * @author rafael
 *
 */

public class App2UsingVariables {
	
	public static void main(String[] args) {
		
		// Declaração de variável: tipo nome-da-variável
		int meuNumeroInteiro;
		// Atribuição de valor à variável
		meuNumeroInteiro = 12;
		
		// Declaração e atribuição de valor
		int meuOutroInteiro = 13;
		
		// Exibindo o valor da variável
		System.out.println(meuNumeroInteiro);
		
		// Exibindo o valor da variável junto com uma mensagem (concatenação)
		System.out.println("O valor do outro inteiro é " + meuOutroInteiro);
		
		
	}

}
