package coreJava;

/**
 * Trabalhando com Loop do tipo if else.
 * @author rafael
 *
 */

public class App6LoopIf {
	
	public static void main(String[] args) {
		
		int idade = 32;
		
		// Início do loop. Se a condição for atendida, faz algo, senão, faz outra coisa.
		if(idade > 18)
		{
			System.out.println("Você já é maior de idade.");
		}
		else
		{
			System.out.println("Você é menor de idade.");
		}
	}

}
